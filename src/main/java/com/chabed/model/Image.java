package com.chabed.model;

public class Image {
		String imageName;
		
		public Image() {
			
		}
		

		public Image(String imageName) {
			super();
			this.imageName = imageName;
		}


		public String getImageName() {
			return imageName;
		}


		public void setImageName(String imageName) {
			this.imageName = imageName;
		}
		
		
}
